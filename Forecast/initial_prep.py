def load_dataset(file_excel: str, skiprows: int = 8) -> pd.DataFrame | None:
    """
    Загружает данные о ремонтах из Excel-файла.
    """
    try:
        data = pd.read_excel(file_excel, skiprows=range(skiprows), dtype=str)
        print(f"Файл успешно загружен из Excel: {file_excel}")
        return data
    except FileNotFoundError:
        print("Файлы не найдены. Проверьте путь к CSV и Excel.")
        return None
    

def extract_model_case_insensitive(machine: str, brand: str) -> str:
    """
    Извлекает модель из строки названия машины, отсекая название бренда.
    """
    if pd.isna(machine) or pd.isna(brand):
        return None

    machine_lower = machine.lower()
    brand_lower = brand.lower()
    idx = machine_lower.find(brand_lower)
    if idx == -1:
        return None

    return machine[idx + len(brand):].strip()


def preprocess_repair_parts(file_path: str, skiprows: int = 8) -> pd.DataFrame:
    """
    Полный цикл предобработки отчета 'Запчасти списанные в ремонт': 
    загружает файл и сразу прогоняет через предобработку.
    """
    data = load_dataset(file_path, skiprows)
    df = data.copy()
    df.columns = df.columns.str.strip()

    df = df[~(df['Номенклатура.Оригинальный номер'].isna() & df['Номенклатура.Артикул'].isna())]
    df = df[df['Машина'].str.contains('подъемник', case=False, na=False)].copy()

    df['Номенклатура'] = df['Номенклатура'].str.strip()

    type_conditions = [
        df['Машина'].str.contains('ножничный', case=False, na=False),
        df['Машина'].str.contains('коленчатый', case=False, na=False),
        df['Машина'].str.contains('телескопический', case=False, na=False),
        df['Машина'].str.contains('мачтовый', case=False, na=False)
    ]

    lift_type_labels = ['ножничный', 'коленчатый', 'телескопический', 'мачтовый']

    df['Тип подъемника'] = np.select(type_conditions, lift_type_labels, default='другое')

    engine_conditions = [
        df['Машина'].str.contains('электрический', case=False, na=False),
        df['Машина'].str.contains('дизельный', case=False, na=False),
    ]

    engine_labels = ['Электрический', 'Дизельный']

    df['Тип двигателя'] = np.select(engine_conditions, engine_labels, default='Дизельный')

    df = df.drop(columns=[col for col in df.columns if 'Unnamed' in col])

    df['Дата'] = pd.to_datetime(df['Дата'], format='%d.%m.%Y %H:%M:%S')
    df['Год'] = df['Дата'].dt.year
    df['Квартал'] = df['Дата'].dt.quarter

    df['Лот.CRM год выпуска'] = df['Лот.CRM год выпуска'].apply(lambda x: int(x.replace(',', '')) if isinstance(x, str) else x)
    df['Средняя наработка (лет)'] = df['Год'] - df['Лот.CRM год выпуска']
    df['Номера машин'] = df.apply(lambda row: extract_model_case_insensitive(row['Машина'], row['Машина.Бренд']), axis=1)

    return df


