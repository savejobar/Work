def load_config(file_name: str) -> dict:
    """
    Загружает конфигурационный JSON из папки configs.
    """
    config_path = os.path.join('configs', file_name)
    if os.path.exists(config_path):
        with open(config_path, 'r', encoding='utf-8') as f:
            return json.load(f)
    return {}


